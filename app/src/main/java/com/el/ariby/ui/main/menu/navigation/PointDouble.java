package com.el.ariby.ui.main.menu.navigation;

public class PointDouble {
    Double x;
    Double y;

    public PointDouble(Double x, Double y) {
        this.x = x;
        this.y = y;
    }

    public Double getX() {
        return x;
    }

    public void setX(Double x) {
        this.x = x;
    }

    public Double getY() {
        return y;
    }

    public void setY(Double y) {
        this.y = y;
    }
}
